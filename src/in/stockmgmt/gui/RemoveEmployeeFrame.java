    private void loadEmpId() {
       try{
            ArrayList<String>empid;
            empid=EmployeeDAO.getAllEmpId();
            for(String x:empid){
                cmbEmpId.addItem(x);
            }
        }
        catch(SQLException ex){
         JOptionPane.showMessageDialog(null, "DB Eror", "Error", JOptionPane.ERROR_MESSAGE);
         ex.printStackTrace();
     }  
    }
