    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int row = jtAllProducts.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(null, "No product selected");
            return;
        }
        String pid = tm.getValueAt(row, 0).toString();
        try {
            if (ProductsDAO.deleteProduct(pid)) {
                JOptionPane.showMessageDialog(null, "Products deleted succeffully");
                tm.setRowCount(0);
                showAllProducts();
                return;
            }
            JOptionPane.showMessageDialog(null, "Products not deleted");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "DB Eror", "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }       
    }//GEN-LAST:event_btnDeleteActionPerformed

