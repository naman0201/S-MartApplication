private void showAllProducts() {
        try {
            List<ProductsPojo> productList = ProductsDAO.getProductDetails();
            if (productList.isEmpty()) {
                JOptionPane.showMessageDialog(null, "No product present in Stock");
                return;
            }
            tm = (DefaultTableModel) jtAllProducts.getModel();
            Object[] rows = new Object[7];
            for (ProductsPojo prod : productList) {
                rows[0] = prod.getProductId();
                rows[1] = prod.getProductName();
                rows[2] = prod.getProductCompany();
                rows[3] = prod.getProductprice();
                rows[4] = prod.getOurPrice();
                rows[5] = prod.getQuantity();
                rows[6] = prod.getTax() + "%";
                
                tm.addRow(rows);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "DB Eror", "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
        
    }
