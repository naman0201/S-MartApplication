    private void jtAllProductsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtAllProductsMouseClicked
        int row=jtAllProducts.getSelectedRow();
        txtProductId.setText(tm.getValueAt(row, 0).toString());
        txtProductName.setText(tm.getValueAt(row, 1).toString());
        txtProductCompany.setText(tm.getValueAt(row, 2).toString());
        txtProductPrice.setText(tm.getValueAt(row, 3).toString());
        txtOurPrice.setText(tm.getValueAt(row, 4).toString());
        txtQuantity.setText(tm.getValueAt(row, 5).toString());
        cmbTax.setSelectedItem(tm.getValueAt(row, 6).toString());
    }//GEN-LAST:event_jtAllProductsMouseClicked
    private boolean validateInputs(){
        return !(txtProductName.getText().isEmpty() || txtProductCompany.getText().isEmpty() || txtProductPrice.getText().isEmpty() || txtOurPrice.getText().isEmpty() || txtProductCompany.getText().isEmpty() || txtQuantity.getText().isEmpty());
    }
    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if(!validateInputs()){
            JOptionPane.showMessageDialog(null, "Please fill all the fields");
            return;
        }
        try{
            int n=cmbTax.getSelectedItem().toString().trim().length();
            int tax=Integer.parseInt(cmbTax.getSelectedItem().toString().substring(0,n-1));
            ProductsPojo p=new ProductsPojo();
            p.setProductId(txtProductId.getText().trim());
            p.setProductName(txtProductName.getText().trim());
            p.setProductCompany(txtProductCompany.getText().trim());
            p.setProductprice(Double.parseDouble(txtProductPrice.getText().trim()));
            p.setOurPrice(Double.parseDouble(txtOurPrice.getText().trim()));
            p.setQuantity(Integer.parseInt(txtQuantity.getText().trim()));
            p.setTax(tax);
            boolean result=ProductsDAO.updateProduct(p);
            if(result){
                JOptionPane.showMessageDialog(null, "Products updated succeffully");
                tm.setRowCount(0);
                showAllProducts();
                return;
            }
            JOptionPane.showMessageDialog(null, "Products updation failed");
        }
        catch(SQLException ex){
         JOptionPane.showMessageDialog(null, "Product Not saved", "Not saved", JOptionPane.ERROR_MESSAGE);
         ex.printStackTrace();
        }
       catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(null, "Please input numeric values", "Conversion Error!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

