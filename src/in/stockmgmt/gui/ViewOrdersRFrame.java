    private void btnShowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnShowActionPerformed
        try {
            if(tm!=null){
            tm.setRowCount(0);
            }
            String ord_id = cmbOId.getSelectedItem().toString();
            List<OrdersPojo> allprod = OrderDAO.getProductsByOrderId(ord_id);
            tm = (DefaultTableModel) jtAllProducts.getModel();
            Object[] rows = new Object[7];
            for (OrdersPojo ord : allprod) {
                rows[0] = ord.getProductId();
                rows[1] = ord.getProductName();
                rows[2] = ord.getProductCompany();
                rows[3] = ord.getProductprice();
                rows[4] = ord.getOurPrice();
                rows[5] = ord.getQuantity();
                rows[6] = ord.getTax() + "%";
                tm.addRow(rows);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "DB Eror", "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnShowActionPerformed

