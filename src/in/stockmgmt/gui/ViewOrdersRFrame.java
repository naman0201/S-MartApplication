    private void loadOrderId() {
        try {
            ArrayList<String> rcp = (ArrayList<String>) OrderDAO.getAllOrderId();
            if (rcp.isEmpty()) {
                JOptionPane.showMessageDialog(null, "No Orders present");
                ManagerOptionsFrame rof=new ManagerOptionsFrame();
                rof.setVisible(true);
                this.dispose();
                return;
            }
            for (String x : rcp) {
                cmbOId.addItem(x);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "DB Eror", "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }
